var good = new Set();
var bad = new Set();

let nbRound;
function hint(){
	if (good.size+bad.size<4||good.size+bad.size===nbRound) {
		return;
	}
	nbRound=good.size+bad.size;
	let pat=$('#mot').text();
	let goodLetters=[...good].join('');
	let re1=new RegExp('^'+pat.replaceAll('\.','[^'+goodLetters+']')+'$');
	let re2=new RegExp('['+$('#lettres').text()+']');
	$('#hint').text(mots.filter(x=>re1.test(x)&&!re2.test(x)));
}

$(document).ready(function() {

	let mot=mots[Math.floor(Math.random()*mots.length)];
  
	for (let i = 0; i<mot.length; i+=1){
		$('#mot').append('<div id="l'+i+'">.</div>');
	}
	
	$('body').on('keyup',keyup);
	$('#hintcont').on('mouseover',hint);

	function keyup(){
		let l=event.key.toUpperCase();
		if(!/^[A-Z]$/.test(l)||bad.size>10) {
			return;
		}
		if (mot.includes(l)) {
			reveal(l);
		} else {
			used(l);
		}
	}

	function reveal(l){
		if ( good.has(l)) {
			return;
		}
		good.add(l);
		Array.from(mot).forEach((c,i)=>{
			if (c===l){
				$('#l'+i).text(l);
			}
		});
		let gagne=/^\w+$/.test($('#mot').text());
		if (gagne){
			$('#nbTry').text('BRAVO !');
			document.body.onkeyup='';
		}
	}

	function used(l){
		if (bad.has(l)) {
			return;
		}
		bad.add(l);
		$('#lettres').append("<div>"+l+"</div>");
		let nbTry = 11-bad.size;
		$('#nbTry').text(nbTry<1?'PERDU ! le mot Ã©tait '+mot:'Nombre d\'essais restants: '+nbTry);
		drawCanvas();
	}

});

